package muhammadhusen.com.ert.result;

public class ResultUser {

    String nama;
    String ttl;
    String no_hp;
    String alamat;


    String username;
    String password;
    String foto_profil;
    String level;


    public String getLevel() {
        return level;
    }

    public String getNama() {
        return nama;
    }

    public String getTtl() {
        return ttl;
    }

    public String getNo_hp() {
        return no_hp;
    }

    public String getAlamat() {
        return alamat;
    }

    public String getUsername() {
        return username;
    }

    public String getPassword() {
        return password;
    }

    public String getFoto_profil() {
        return foto_profil;
    }
}
